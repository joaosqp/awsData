# Adição (+): Usado para somar dois valores.
a = 5
b = 3
resultado = a + b
print(resultado)

# Subtração (-): Usado para subtrair um valor de outro.
a = 10
b = 4
resultado = a - b
print(resultado) 

# Multiplicação (*): Usado para multiplicar dois valores.
a = 7
b = 2
resultado = a * b
print(resultado)  

# Divisão (/): Usado para dividir um valor pelo outro (geralmente resulta em um número de ponto flutuante).
a = 15
b = 3
resultado = a / b
print(resultado) 

# Divisão inteira (//): Realiza a divisão e arredonda o resultado para baixo para o número inteiro mais próximo.
a = 15
b = 4
resultado = a // b
print(resultado) 

# Módulo (%): Retorna o resto da divisão entre dois valores.
a = 17
b = 5
resultado = a % b
print(resultado)  # Output: 2

# Potência (**): Calcula a potência de um número elevado a outro.
a = 2
b = 3
resultado = a ** b
print(resultado)